def score_property_lead(lead_type: str, days_on_market: int = 0,
                        price_reduction_pct: float = 0.0) -> str:
    """Rule-based scoring — no AI needed, rules are explicit."""
    if lead_type in ("PRE_FORECLOSURE", "LLC_PURCHASE", "NEW_CONSTRUCTION", "EXPIRED", "BACK_ON_MARKET"):
        return "HOT"
    if lead_type == "CASH_BUYER":
        return "WARM"
    if lead_type == "FSBO":
        return "HOT" if days_on_market >= 30 else "WARM"
    if lead_type == "PRICE_DROP":
        return "HOT" if price_reduction_pct >= 5.0 else "WARM"
    return "WARM"
